#include "task_manager/task_factory.h"

Task* new_task(String key){
	if (key == LSM6DSOX_DRIVER_KEY) {
		return new LSM6DSOX();
	}
	else if (key == COMPFLTR_DRIVER_KEY) {
		return new ComplimentaryFilter();
	}
	else {
		return nullptr;
	}
}
